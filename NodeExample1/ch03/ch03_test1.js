/* [ 자바스크립트의 자료형 ]
 * - Boolean 	: true or false (기본 자료형)
 * - Number 	: 정수나 부동소수 값 (기본 자료형)
 * - String  	: 문자열 값 (기본 자료형)
 * - undefined 	: 값을 할당하지 않은 변수의 값
 * - null 		: 존재하지 않는 값을 가리키는 값
 * - Object		: 객체를 값으로 가지는 자료형
 */

